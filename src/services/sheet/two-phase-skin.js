export function getTwoPhaseSkinSpeeds(g,δ1,δ2,p1,p2,υ1,υ2,μ1,μ2,Ge ) {
    //g – прискорення вільного падіння
    //p – густина рідини; 
    //υ i μ - кінематичний та динамічний коефіцієнти в’язкості;
    //Ge поток газа
    //δ ширина пленки
    let u1=(g/υ1)*(-(y*y/2)+(δ1+(p2*δ1/p1)*(1-Ge))*y)
    let u2=(g/υ2)*(-(y*y/2)+(δ1+δ2*(1-Ge)*y+(δ1*δ1/2)*((υ2/υ1)-1)+δ1*δ2*((μ2/μ1)-1)*(1-Ge)))
    let speed={
        u1,u2
    }
    return speed
}